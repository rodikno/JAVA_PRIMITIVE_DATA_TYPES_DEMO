public class CharAsIntegersDemo {

    public static void main(String[] args) {
        char myChar = 'A'; // ASCII value 65
        System.out.println("Char value: " + myChar);
        System.out.println("Char as int: " + (int) myChar);
        System.out.println("Next char: " + (char) (myChar + 1));

    }
}
